package assignments;


public class Assignment_9 
{
	/*
	 * Write a Java program that uses nested loops to print a number pattern as shown below based on the number of rows entered by the user.
	 * 1
     * 22
     * 333
     * 4444
     * 55555 
	 */
	public static void main(String args[])
	{
		for(int i=1;i<=5;i++)
		{
			for(int j=1;j<=i;j++)
			{
				System.out.print(i);
			}
		System.out.println();	
		
		}
	
	}
	
}
